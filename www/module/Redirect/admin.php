class RedirectModuleEdit extends BMC_BaseModule 
{
	/**
	 * ??????? ??? ???????? html-???? ??????
	 * 
	 * @return void
	 */
	function DataBind()
	{
		$RedirectDb = new DAL_RedirectDb();
        $URL=$RedirectDb->GetUrl($this->moduleId);
        $this->data['RedirectUrl']=$URL['URL'];

	}
	
	function __construct($moduleId)
	{
		parent::__construct($moduleId);
	}
	
	/**
	 * ????????? ??????
	 */
	public function handlerBtnSave()
	{
		$text = $this->data['RedirectUrl'];
        $RedirectDb = new DAL_RedirectDb();
       $RedirectDb->UpdateUrl($this->moduleId,$text);
	} 
}
